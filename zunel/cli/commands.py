@app.command()
def status():
    """Show zunel status."""
    from zunel.config.loader import get_config_path, load_config

    config_path = get_config_path()
    config = load_config()
    workspace = config.workspace_path

    console.print(f"{__logo__} zunel Status\n")

    console.print(f"Config: {config_path} {'[green]✓[/green]' if config_path.exists() else '[red]✗[/red]'}")
    console.print(f"Workspace: {workspace} {'[green]✓[/green]' if workspace.exists() else '[red]✗[/red]'}")

    if config_path.exists():
        console.print(f"Model: {config.agents.defaults.model}")

        custom = config.providers.custom
        has_key = bool(custom.api_key)
        api_base = custom.api_base or "(default)"
        status_marker = "[green]✓[/green]" if has_key else "[dim]not set[/dim]"
        console.print(f"Custom provider: {status_marker}")
        if has_key:
            console.print(f"  API base: {api_base}")


